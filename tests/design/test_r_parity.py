# Fixture 8: Basis functions
# ============================================================================
class TestBasisFunctions:
    """Test Poly, BSpline, Scale, RobustScale basis functions."""

    @pytest.fixture(autouse=True)
    def setup(self):
        self.fix = load_fixture("fixture_basis_functions")

    def test_poly_basis(self):
        from fmrimod import Poly

        x = np.array(self.fix["x"])
        r_mat = np.array(self.fix["poly"]["matrix"])

        py_poly = Poly.from_data(x, degree=3)
        py_mat = py_poly.y

        assert py_mat.shape == r_mat.shape, (
            f"Poly shape mismatch: {py_mat.shape} vs {r_mat.shape}"
        )
        # Poly basis may differ by sign (eigenvector sign ambiguity)
        for col in range(r_mat.shape[1]):
            corr = np.corrcoef(py_mat[:, col], r_mat[:, col])[0, 1]
            assert abs(abs(corr) - 1.0) < 1e-4, (
                f"Poly column {col} not correlated: r={corr}"
            )

    def test_scale_basis(self):
        from fmrimod import Scale

        x = np.array(self.fix["x"])
        r_vals = np.array(self.fix["scale"]["values"])

        py_scale = Scale.from_data(x)
        py_vals = np.asarray(py_scale.y).ravel()

        np.testing.assert_allclose(py_vals, r_vals, atol=ATOL,
                                   err_msg="Scale basis mismatch")

    def test_robust_scale_basis(self):
        from fmrimod import RobustScale

        x = np.array(self.fix["x"])
        r_vals = np.array(self.fix["robust_scale"]["values"])

        py_robust = RobustScale.from_data(x)
        py_vals = np.asarray(py_robust.y).ravel()

        np.testing.assert_allclose(py_vals, r_vals, atol=ATOL,
                                   err_msg="RobustScale basis mismatch")


