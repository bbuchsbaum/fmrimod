# Fixture 1: Simple HRF convolution (spmg1)
# ============================================================================
class TestSimpleHRF:
    """Test spmg1 HRF convolution with 2 conditions."""

    @pytest.fixture(autouse=True)
    def setup(self):
        self.fix = load_fixture("fixture_simple_hrf")

    def test_design_matrix_shape(self):
        r_dm = np.array(self.fix["design_matrix"]["matrix"])
        assert r_dm.shape == (
            self.fix["design_matrix"]["nrow"],
            self.fix["design_matrix"]["ncol"],
        )

    @pytest.mark.xfail(
        reason=(
            "fmrimod's default SPM canonical was realigned to SPM/Nilearn "
            "standard parameterization (early 2026); the R fixture pins "
            "the legacy R-fmrireg shape. Tracking issue on "
            "bbuchsbaum/fmrihrf will bring R into alignment; until then "
            "this test can be flipped to basis='spm_legacy' for R parity."
        ),
        strict=True,
    )
    def test_design_matrix_values(self):
        from fmrimod import event_model, design_matrix

        df = _events_to_df(self.fix["events"])
        sf = _make_sampling_frame(self.fix["sampling_frame"])

        model = event_model(
            "onset ~ hrf(condition, basis='spmg1')",
            data=df,
            block="run",
            sampling_frame=sf,
            durations=self.fix["model_info"]["durations"],
            precision=self.fix["model_info"]["precision"],
        )

        py_dm = design_matrix(model)
        r_dm = np.array(self.fix["design_matrix"]["matrix"])

        assert py_dm.shape == r_dm.shape, (
            f"Shape mismatch: Python {py_dm.shape} vs R {r_dm.shape}"
        )
        np.testing.assert_allclose(py_dm, r_dm, atol=ATOL,
                                   err_msg="spmg1 design matrix mismatch")

    def test_column_names(self):
        from fmrimod import event_model

        df = _events_to_df(self.fix["events"])
        sf = _make_sampling_frame(self.fix["sampling_frame"])

        model = event_model(
            "onset ~ hrf(condition, basis='spmg1')",
            data=df,
            block="run",
            sampling_frame=sf,
            durations=self.fix["model_info"]["durations"],
            precision=self.fix["model_info"]["precision"],
        )

        r_colnames = self.fix["design_matrix"]["colnames"]
        py_colnames = list(model.column_names)
        assert py_colnames == r_colnames, (
            f"Column names mismatch:\n  Python: {py_colnames}\n  R: {r_colnames}"
        )


