"""Tests for numerical parity between R fmridesign and Python pyfmridesign.

Each test loads a JSON fixture generated by generate_fixtures.R and compares
the Python output against R golden reference values.

Fixtures are generated by running: Rscript tests/generate_fixtures.R

SPM canonical alignment (early 2026) intentionally diverged fmrimod's
default ``basis="spm"`` HRF from the R ``fmrireg`` parameterization to
match SPM ``spm_hrf`` and Nilearn ``_gamma_difference_hrf``. Until the
R-side fixtures are regenerated against an R fmrireg that adopts the
SPM standard parameterization (tracked in the bbuchsbaum/fmrihrf
issue queue alongside the dispersion-derivative fix), R parity tests
that pin SPMG-realised values are marked ``xfail(strict=True)``. The
legacy R parameterization stays available via ``basis="spm_legacy"``
and can be substituted into these tests for full R parity if needed.
"""

import json
from pathlib import Path

import numpy as np
import pandas as pd
import pytest

FIXTURE_DIR = Path(__file__).parent / "fixtures"
ATOL = 1e-6  # absolute tolerance for numeric comparisons


def load_fixture(name: str) -> dict:
    """Load a JSON fixture file."""
    path = FIXTURE_DIR / f"{name}.json"
    if not path.exists():
        pytest.skip(f"Fixture {name}.json not found; run generate_fixtures.R first")
    with open(path) as f:
        return json.load(f)


def _make_sampling_frame(sf_info):
    """Create a SamplingFrame from fixture sampling_frame info."""
    from fmrimod import SamplingFrame
    blocklens = sf_info["blocklens"]
    if not isinstance(blocklens, list):
        blocklens = [blocklens]
    return SamplingFrame(blocklens=blocklens, TR=sf_info["TR"])


def _events_to_df(events) -> pd.DataFrame:
    """Convert fixture events (list of dicts or dict of lists) to DataFrame."""
    return pd.DataFrame(events)


